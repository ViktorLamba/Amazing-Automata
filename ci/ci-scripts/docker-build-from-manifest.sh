#!/usr/bin/env bash
set -euo pipefail

MANIFEST=${1:-project/manifest.json}
IMAGE_NAME=${2:-myorg/myapp:latest}    # можно переопределить аргументом
PUSH=${3:-false}                       # true|false
LOAD_LOCAL=${4:-false}                 # true|false (если нужно --load вместо push)
BUILDX=${5:-true}                      # use buildx by default

if ! command -v jq >/dev/null 2>&1; then
  echo "❌ Require jq installed" >&2
  exit 2
fi

if [ ! -f "$MANIFEST" ]; then
  echo "❌ Manifest not found: $MANIFEST" >&2
  exit 2
fi

# читаем manifest
LANGUAGE=$(jq -r '.language // "unknown"' "$MANIFEST")
HAS_DOCKERFILE=$(jq -r '.has_dockerfile // false' "$MANIFEST")
ENTRY=$(jq -r '.entry // empty' "$MANIFEST")
TARGETS=$(jq -r '.targets | join(",") // "linux/amd64"' "$MANIFEST")
PROJECT_DIR=$(dirname "$MANIFEST")

echo "📦 Language: $LANGUAGE"
echo "📄 Dockerfile present: $HAS_DOCKERFILE"
echo "➡️  Entry (raw): $ENTRY"
echo "📂 Project dir: $PROJECT_DIR"
echo "🎯 Targets: $TARGETS"

# ensure project dir exists
if [ ! -d "$PROJECT_DIR" ]; then
  echo "❌ Project directory not found: $PROJECT_DIR" >&2
  exit 2
fi

# normalize ENTRY relative to project dir
if [ -n "$ENTRY" ]; then
  ENTRY_REL=$(realpath --relative-to="$PROJECT_DIR" "$ENTRY" 2>/dev/null || echo "$ENTRY")
else
  ENTRY_REL=""
fi
echo "➡️  ENTRY relative: $ENTRY_REL"

# Если проект сам содержит Dockerfile
if [ "$HAS_DOCKERFILE" = "true" ] && [ -f "$PROJECT_DIR/Dockerfile" ]; then
  echo "✅ Using project's Dockerfile: $PROJECT_DIR/Dockerfile"
  DOCKERFILE_PATH="$PROJECT_DIR/Dockerfile"
else
  echo "⚙️  No Dockerfile -> generating temporary Dockerfile.ci for language=$LANGUAGE"
  DOCKERFILE_PATH="$PROJECT_DIR/Dockerfile.ci"

  cat > "$DOCKERFILE_PATH" <<'EOF'
# Temporary Dockerfile generated by build.sh
EOF

  case "$LANGUAGE" in
    python)
      cat >> "$DOCKERFILE_PATH" <<EOF
FROM python:3.11-slim
WORKDIR /app
COPY . .
RUN pip install --no-cache-dir -r requirements.txt || true
EOF
      if [ -n "$ENTRY_REL" ]; then
        echo "CMD [\"python\", \"./$ENTRY_REL\"]" >> "$DOCKERFILE_PATH"
      else
        echo "CMD [\"sh\", \"-c\", \"ls -la /app\"]" >> "$DOCKERFILE_PATH"
      fi
      ;;
    go)
      cat >> "$DOCKERFILE_PATH" <<EOF
FROM golang:1.20-alpine AS build
WORKDIR /app
COPY . .
RUN apk add --no-cache build-base git && go mod tidy && go build -o app .

FROM alpine:3.18
WORKDIR /app
COPY --from=build /app/app .
ENTRYPOINT ["./app"]
EOF
      ;;
    cpp|c++)
      cat >> "$DOCKERFILE_PATH" <<EOF
FROM debian:bookworm-slim AS build

# Устанавливаем инструменты для сборки
RUN apt-get update && apt-get install -y g++ cmake make

WORKDIR /app
COPY . .

# Собираем проект через CMake
WORKDIR /app/project/test
RUN mkdir -p build && cd build && cmake .. && make -j4

# Runtime образ
FROM debian:bookworm-slim
WORKDIR /app
COPY --from=build /app/project/test/build/test ./test
ENTRYPOINT ["./test"]

EOF
      ;;
    rust)
      cat >> "$DOCKERFILE_PATH" <<EOF
FROM rust:1.72 AS build
WORKDIR /app
COPY . .
RUN cargo build --release
FROM debian:bookworm-slim
WORKDIR /app
COPY --from=build /app/target/release/* /usr/local/bin/
CMD ["sh", "-c", "ls -la /usr/local/bin && echo 'Run your binary here'"]
EOF
      ;;
    node)
      cat >> "$DOCKERFILE_PATH" <<EOF
FROM node:18-alpine
WORKDIR /app
COPY --from=build /app .
ENTRYPOINT ["dotnet", "YourApp.dll"]
EOF
      ;;
    cpp)
      cat >> "$DOCKERFILE_PATH" <<'EOF'
# Build stage for C++ (CMake)
FROM ubuntu:24.04 AS builder
ENV DEBIAN_FRONTEND=noninteractive
WORKDIR /src

# install build deps
RUN apt-get update && apt-get install -y --no-install-recommends \
    build-essential cmake git pkg-config ca-certificates && \
    rm -rf /var/lib/apt/lists/*

COPY . .

# ensure possible dirs exist so COPY from builder won't fail
RUN mkdir -p build example bench

# (Optional) try to compile an explicit provided .cpp later (generator may insert a compile step)
# configure & build (attempt; don't fail whole build if project has no executable)
RUN mkdir -p build && cd build && cmake .. && make -j$(nproc) || true

# runtime stage
FROM ubuntu:24.04 AS runtime
WORKDIR /app

# create non-root user
RUN adduser --disabled-password --gecos "" appuser || true

# prepare runtime bin dir
RUN mkdir -p /usr/local/bin

# copy potential build outputs from builder (directories guaranteed to exist)
COPY --from=builder /src/build /tmp/build
COPY --from=builder /src/example /tmp/example
COPY --from=builder /src/bench /tmp/bench

# find executables and copy them into /usr/local/bin
RUN set -eux; \
    for d in /tmp/build /tmp/example /tmp/bench; do \
      if [ -d "$d" ]; then \
        find "$d" -type f -executable -exec cp {} /usr/local/bin/ \; || true; \
      fi; \
    done; \
    ls -la /usr/local/bin || true

# create entrypoint script
RUN cat > /usr/local/bin/ci-entrypoint.sh <<'SH'
#!/bin/sh
set -e
if [ -n "${ENTRY}" ]; then
  exec sh -c "${ENTRY}"
fi
BIN=$(ls /usr/local/bin | head -n1 || true)
if [ -n "$BIN" ]; then
  exec /usr/local/bin/"$BIN"
else
  echo "No executable found in image"
  sleep infinity
fi
SH
RUN chmod +x /usr/local/bin/ci-entrypoint.sh

CMD ["/usr/local/bin/ci-entrypoint.sh"]
COPY package*.json . || true
RUN npm install || true
COPY . .
EOF
      if [ -n "$ENTRY_REL" ]; then
        echo "CMD [\"node\", \"./$ENTRY_REL\"]" >> "$DOCKERFILE_PATH"
      else
        echo "CMD [\"npm\", \"start\"]" >> "$DOCKERFILE_PATH"
      fi
      ;;
    *)
      echo "⚠️  Language $LANGUAGE not recognized; using generic Alpine image"
      cat >> "$DOCKERFILE_PATH" <<EOF
FROM alpine:3.18
WORKDIR /app
COPY . .
CMD ["sh", "-c", "ls -la && echo 'No start command defined'"]
EOF
      ;;
  esac

  # --- подставляем ENTRY ---
  ENTRY_REL="${ENTRY#$PROJECT_DIR/}"   # убираем префикс project/ (если есть)
  echo "ℹ️ ENTRY_REL resolved to: $ENTRY_REL"

  # Если ENTRY указывает на исходник .cpp — вставим команду компиляции в builder перед configure (через sed)
  if [ -n "$ENTRY_REL" ] && printf '%s' "$ENTRY_REL" | grep -qE '\.cpp$'; then
    echo "ℹ️ Detected .cpp entry -> will compile it in builder and use /usr/local/bin/ci_entry as runtime entry"
    # вставим строку после строки "# ensure possible dirs exist so COPY from builder won't fail"
    # добавим команду: RUN mkdir -p build && g++ -std=c++17 "<ENTRY_REL>" -o build/ci_entry || true
    # в Dockerfile, заменяем маркер ${ENTRY} позже на /usr/local/bin/ci_entry
    # используем sed с учётом платформы
    compile_cmd="RUN mkdir -p build && g++ -std=c++17 \"$ENTRY_REL\" -o build/ci_entry || true"
    if sed --version >/dev/null 2>&1; then
      # GNU sed
      sed -i "/# ensure possible dirs exist so COPY from builder won't fail/a $compile_cmd" "$DOCKERFILE_PATH"
    else
      # BSD/mac sed (rare on CI but included for portability)
      sed -i '' "/# ensure possible dirs exist so COPY from builder won't fail/a\\
$compile_cmd
" "$DOCKERFILE_PATH"
    fi

    # В рантайме мы копируем /src/build -> /tmp/build и затем cp в /usr/local/bin.
    # поэтому укажем ENTRY_rel как /usr/local/bin/ci_entry
    ENTRY_REL="/usr/local/bin/ci_entry"
  fi

  # Подставляем ENTRY_REL в Dockerfile
  if [ -n "$ENTRY_REL" ]; then
    if sed --version >/dev/null 2>&1; then
      sed -i "s|\${ENTRY}|$ENTRY_REL|g" "$DOCKERFILE_PATH"
    else
      sed -i '' "s|\${ENTRY}|$ENTRY_REL|g" "$DOCKERFILE_PATH"
    fi
  fi
fi

# Build image
PLATFORMS="$TARGETS"

if [ "$BUILDX" = "true" ]; then
  docker buildx inspect >/dev/null 2>&1 || docker buildx create --use
  if [ "$PUSH" = "true" ]; then
    docker buildx build --platform "$PLATFORMS" -t "$IMAGE_NAME" --push -f "$DOCKERFILE_PATH" "$PROJECT_DIR"
  else
    if [ "$LOAD_LOCAL" = "true" ]; then
      docker buildx build --platform "$PLATFORMS" -t "$IMAGE_NAME" --load -f "$DOCKERFILE_PATH" "$PROJECT_DIR"
    else
      docker buildx build --platform "$PLATFORMS" -t "$IMAGE_NAME" -f "$DOCKERFILE_PATH" "$PROJECT_DIR"
    fi
  fi
else
  docker build -f "$DOCKERFILE_PATH" -t "$IMAGE_NAME" "$PROJECT_DIR"
  if [ "$PUSH" = "true" ]; then
    docker push "$IMAGE_NAME"
  fi
fi

# Clean up
if [ "$DOCKERFILE_PATH" = "$PROJECT_DIR/Dockerfile.ci" ]; then
  rm -f "$DOCKERFILE_PATH"
fi

echo "✅ Done. Built $IMAGE_NAME"
