#!/usr/bin/env bash
set -euo pipefail

MANIFEST=${1:-project/manifest.json}
IMAGE_NAME=${2:-myorg/myapp:latest}
PUSH=${3:-false}
LOAD_LOCAL=${4:-false}
BUILDX=${5:-true}

if ! command -v jq >/dev/null 2>&1; then
  echo "❌ Require jq installed" >&2
  exit 2
fi

if [ ! -f "$MANIFEST" ]; then
  echo "❌ Manifest not found: $MANIFEST" >&2
  exit 2
fi

LANGUAGE=$(jq -r '.language // "unknown"' "$MANIFEST")
HAS_DOCKERFILE=$(jq -r '.has_dockerfile // false' "$MANIFEST")
ENTRY=$(jq -r '.entry // empty' "$MANIFEST")
DEPS_FILE=$(jq -r '.deps_file // empty' "$MANIFEST")
TARGETS=$(jq -r '.targets | join(",") // "linux/amd64"' "$MANIFEST")
PROJECT_DIR=$(dirname "$MANIFEST")

echo "📦 Language: $LANGUAGE"
echo "📄 Dockerfile present: $HAS_DOCKERFILE"
echo "➡️  Entry: $ENTRY"
[ -n "$DEPS_FILE" ] && echo "📦 Deps file: $DEPS_FILE"
echo "📂 Project dir: $PROJECT_DIR"
echo "🎯 Targets: $TARGETS"

if [ ! -d "$PROJECT_DIR" ]; then
  echo "❌ Project directory not found: $PROJECT_DIR" >&2
  exit 2
fi

# --- Используем Dockerfile проекта или генерируем временный ---
if [ "$HAS_DOCKERFILE" = "true" ] && [ -f "$PROJECT_DIR/Dockerfile" ]; then
  echo "✅ Using project's Dockerfile"
  DOCKERFILE_PATH="$PROJECT_DIR/Dockerfile"
else
  echo "⚙️ Generating temporary Dockerfile for $LANGUAGE"
  DOCKERFILE_PATH="$PROJECT_DIR/Dockerfile.ci"
  cat > "$DOCKERFILE_PATH" <<'EOF'
# Temporary Dockerfile generated by ci-scripts
EOF

  case "$LANGUAGE" in
    python)
      cat >> "$DOCKERFILE_PATH" <<EOF
FROM python:3.11

# Копируем весь проект
COPY . .

# Установка pip
RUN python -m pip install --upgrade pip
EOF

      if [ -n "$DEPS_FILE" ]; then
        cat >> "$DOCKERFILE_PATH" <<EOF
RUN pip install -r "$DEPS_FILE"
EOF
      fi

      cat >> "$DOCKERFILE_PATH" <<EOF
CMD ["python", "$ENTRY"]
EOF
      ;;
    go)
      # --- Остальной код для Go без изменений ---
      cat >> "$DOCKERFILE_PATH" <<'EOF'
FROM golang:1.20-alpine AS build
WORKDIR /src
COPY . /src
RUN apk add --no-cache build-base git && go mod download && CGO_ENABLED=0 GOOS=linux GOARCH=amd64 go build -o /out/app ./...

FROM alpine:3.18
COPY --from=build /out/app /usr/local/bin/app
CMD ["/usr/local/bin/app"]
EOF
      ;;
    rust)
      # --- Rust без изменений ---
      cat >> "$DOCKERFILE_PATH" <<'EOF'
FROM rust:1.72 AS builder
WORKDIR /src
COPY . /src
RUN cargo build --release || true

FROM debian:bookworm-slim
COPY --from=builder /src/target/release/* /usr/local/bin/
CMD ["sh", "-c", "/usr/local/bin/$(basename ${ENTRY} .rs)"]
EOF
      ;;
    cpp)
      # --- C++ без изменений ---
      cat >> "$DOCKERFILE_PATH" <<'EOF'
FROM ubuntu:24.04 AS builder
WORKDIR /src
RUN apt-get update && apt-get install -y --no-install-recommends \
    build-essential cmake git pkg-config ca-certificates && rm -rf /var/lib/apt/lists/*
COPY . /src
RUN mkdir -p build && cd build && cmake .. && make -j$(nproc) || true

FROM ubuntu:24.04 AS runtime
RUN mkdir -p /usr/local/bin
COPY --from=builder /src/build /tmp/build
RUN for f in /tmp/build/*; do [ -x "$f" ] && cp "$f" /usr/local/bin/ || true; done
CMD ["/usr/local/bin/$(basename ${ENTRY} .cpp)"]
EOF
      ;;
    *)
      # --- fallback ---
      cat >> "$DOCKERFILE_PATH" <<EOF
FROM alpine:3.18
COPY . .
CMD ["sh", "-c", "ls -la && echo 'No start command defined'"]
EOF
      ;;
  esac
fi

# --- Build image ---
PLATFORMS="$TARGETS"
if [ "$BUILDX" = "true" ]; then
  docker buildx inspect >/dev/null 2>&1 || docker buildx create --use
  if [ "$PUSH" = "true" ]; then
    docker buildx build --platform "$PLATFORMS" -t "$IMAGE_NAME" --push -f "$DOCKERFILE_PATH" "$PROJECT_DIR"
  else
    if [ "$LOAD_LOCAL" = "true" ]; then
      docker buildx build --platform "$PLATFORMS" -t "$IMAGE_NAME" --load -f "$DOCKERFILE_PATH" "$PROJECT_DIR"
    else
      docker buildx build --platform "$PLATFORMS" -t "$IMAGE_NAME" -f "$DOCKERFILE_PATH" "$PROJECT_DIR"
    fi
  fi
else
  docker build -f "$DOCKERFILE_PATH" -t "$IMAGE_NAME" "$PROJECT_DIR"
  [ "$PUSH" = "true" ] && docker push "$IMAGE_NAME"
fi

# Удаляем временный Dockerfile
[ "$DOCKERFILE_PATH" = "$PROJECT_DIR/Dockerfile.ci" ] && rm -f "$DOCKERFILE_PATH"
echo "✅ Done."
